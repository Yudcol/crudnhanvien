    private void Xoa(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Xoa
        dongChon = listNV.getSelectedRow(); 
        if (dongChon != -1) {
            int ret=JOptionPane.showConfirmDialog(null,
                "bạn chắc chắn muốn xoá?", "Thoát",
                JOptionPane.YES_NO_OPTION);
            if(ret==JOptionPane.YES_OPTION){
                nvlist.remove(dongChon);
                loadTableNhanVien();
                listNV.getColumn("Ảnh").setCellRenderer(new myTableCellRenderer());
            }
        }
        else
            JOptionPane.showMessageDialog(this, 
                    "Chưa chọn dòng xóa","Thông báo", WIDTH);
    }//GEN-LAST:event_Xoa
    

