    private void sua(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sua
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        SimpleDateFormat sdf2 = new SimpleDateFormat("ddMMyyyy");
        
        int index = listNV.getSelectedRow();
        if(listNV.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this,
                    "Chưa chọn dòng","Thông báo", WIDTH);
        }
        else {
           
            try {
                sua.setVisible(true);
                sua.pack();
                sua.setLocationRelativeTo(null);
                sua.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
                sua.rollno.setText(nvlist.get(index).getId()+"");
                sua.name.setText(nvlist.get(index).getName()+"");
                sua.cid.setText(nvlist.get(index).getCid()+"");
                Date date = sdf2.parse(nvlist.get(index).getBirth());
                sua.birth.setText(sdf.format(date));
                if(nvlist.get(index).getSex() == 0) {
                    sua.radnam.setSelected(true);
                }
                if(nvlist.get(index).getSex() == 1) 
                    sua.radnu.setSelected(true);
                    sua.address.setText(nvlist.get(index).getAddress()+"");
                    sua.national.setText(nvlist.get(index).getNation()+"");
                    sua.religion.setText(nvlist.get(index).getReligion()+"");
                    sua.department.setSelectedIndex(nvlist.get(index).getDepartment());
                    sua.position.setSelectedIndex(nvlist.get(index).getPosition());
                if(nvlist.get(index).getContract() == 0) {
                    sua.radfull.setSelected(true);
                }
                if(nvlist.get(index).getContract() == 1) {
                    sua.radpart.setSelected(true);
                }
                Date date2 = sdf2.parse(nvlist.get(index).getContractTime());
                sua.exp.setText(sdf.format(date2));
                ImageIcon img = new ImageIcon(nvlist.get(index).getImgpath());
                Image imageresize = img.getImage().getScaledInstance(170, 170, Image.SCALE_SMOOTH);
                sua.avatar.setIcon(new ImageIcon(imageresize));
            } catch (ParseException ex) {
                Logger.getLogger(TongQuanHoSo.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_sua

